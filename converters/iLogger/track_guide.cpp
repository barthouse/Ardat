static const int kNumTrackMarkers = sizeof(g_trackMarkers) / sizeof(g_trackMarkers[0]);

TrackGuide::TrackGuide(void)
{
	m_active = false;

	m_crossOutput = NULL;
	m_trajectoryOutput = NULL;
	m_intersectOutput = NULL;
}

void TrackGuide::Start()
{
	m_active = true;
	m_firstSample = true;
	m_lapCount = 0;
	m_markerCount = 0;

	// output marker plot file
	FILE * output = fopen("c:\\gnuplot\\marker.dat", "w+");
	for (int index = 0; index < kNumTrackMarkers; index++)
	{
		fprintf(output, "%f %f\n%f %f\n\n", 
			g_trackMarkers[index].m_leftX,
			g_trackMarkers[index].m_leftY,
			g_trackMarkers[index].m_rightX,
			g_trackMarkers[index].m_rightY);
	}
	fclose(output);

	NewLap();
}

void TrackGuide::NewLap(void)
{
	m_lapCount++;

	ClosePlots();
	OpenPlots();

}

void TrackGuide::OpenPlots(void)
{
	char filename[128];

	assert(m_crossOutput == NULL);
	sprintf(filename, "c:\\gnuplot\\cross.%d.dat", m_lapCount);
	m_crossOutput = fopen(filename, "w+");

	assert(m_trajectoryOutput == NULL);
	sprintf(filename, "c:\\gnuplot\\trajectory.%d.dat", m_lapCount);
	m_trajectoryOutput = fopen(filename, "w+");

	assert(m_intersectOutput == NULL);
	sprintf(filename, "c:\\gnuplot\\intersect.%d.dat", m_lapCount);
	m_intersectOutput = fopen(filename, "w+");
}

void TrackGuide::ClosePlots(void)
{
	if(m_crossOutput != NULL)
	{
		fclose(m_crossOutput);
		m_crossOutput = NULL;
	}


	if(m_trajectoryOutput != NULL)
	{
		fclose(m_trajectoryOutput);
		m_trajectoryOutput = NULL;
	}

	if(m_intersectOutput != NULL)
	{
		fclose(m_intersectOutput);
		m_intersectOutput = NULL;
	}
}

void TrackGuide::Stop()
{
	ClosePlots();

	m_active = false;
}

void TrackGuide::Sample(double inTrackPercentage, double inX, double inY, double inZ, double inSpeed, double * outXOffset, double * outYOffset)
{
	assert(inTrackPercentage < 1.0);

	*outXOffset = 0;
	*outYOffset = 0;

	if (m_active)
	{
		if (m_firstSample)
		{
			// find the next marker that will come up
			for(m_nextMarker = 0; m_nextMarker < kNumTrackMarkers; m_nextMarker++)
			{
				if( g_trackMarkers[m_nextMarker].m_trackPercentage > inTrackPercentage)
					break;
			}

			assert(m_nextMarker!=kNumTrackMarkers);

			if (m_nextMarker == kNumTrackMarkers) m_nextMarker = 0;
			
			m_firstSample = false;
		}
		else
		{
			// check for wrap around and correct saved last track percentage
			if (inTrackPercentage < 0.1 && m_lastTrackPercentage > 0.9)
			{
				m_lastTrackPercentage -= 1.0;
			}

			TrackMarker * trackMarker = &g_trackMarkers[m_nextMarker];

			// have we crossed the next marker
			if (m_lastTrackPercentage < trackMarker->m_trackPercentage &&
				inTrackPercentage >= trackMarker->m_trackPercentage)
			{
				if (inSpeed >= 10.0 && m_markerCount > 100)
				{
					double lerp = (trackMarker->m_trackPercentage - m_lastTrackPercentage) / (inTrackPercentage - m_lastTrackPercentage);

					double x = scalar_lerp(m_lastX, inX, lerp);
					double y = scalar_lerp(m_lastY, inY, lerp);
//					double z = scalar_lerp(m_lastZ, inZ, lerp);

					fprintf(m_crossOutput, "%f %f\n", x, y);

					//
					// Find intersection between our trajectory and marker
					//

					Line2d trajectory(m_lastX, m_lastY, inX, inY);

					fprintf(m_trajectoryOutput, "%f %f\n%f %f\n\n", 
						trajectory.m_u.m_x, 
						trajectory.m_u.m_y, 
						(trajectory.m_u.m_x + trajectory.m_v.m_x * 10), 
						(trajectory.m_u.m_y + trajectory.m_v.m_y * 10));

					Line2d markerLine(trackMarker->m_leftX, trackMarker->m_leftY, trackMarker->m_rightX, trackMarker->m_rightY);

					Vector2d intersectionPoint;

					if (markerLine.Intersect(trajectory, intersectionPoint))
					{
						*outXOffset = intersectionPoint.m_x - x;
						*outYOffset = intersectionPoint.m_y - y;

						fprintf(m_intersectOutput, "%f %f\n", intersectionPoint.m_x, intersectionPoint.m_y);
					}
				}

				m_nextMarker++;
				m_markerCount++;

				if (m_nextMarker >= kNumTrackMarkers) 
				{
					m_nextMarker = 0;
					NewLap();
				}
			}
		}

		m_lastTrackPercentage = inTrackPercentage;
		m_lastX = inX;
		m_lastY = inY;
		m_lastZ = inZ;
	}
}

